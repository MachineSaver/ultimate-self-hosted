process_templates() {
  step "Processing configuration templates"

  find config -name "*.template" | while read -r tmpl; do
    local output="${tmpl%.template}"
    sed \
      -e "s|{{DOMAIN}}|${DOMAIN}|g" \
      -e "s|{{ADMIN_USER}}|${ADMIN_USER}|g" \
      -e "s|{{ADMIN_EMAIL}}|${ADMIN_EMAIL}|g" \
      -e "s|{{POSTGRES_PASSWORD}}|${POSTGRES_PASSWORD}|g" \
      -e "s|{{REDIS_PASSWORD}}|${REDIS_PASSWORD}|g" \
      -e "s|{{AUTHENTIK_SECRET_KEY}}|${AUTHENTIK_SECRET_KEY}|g" \
      -e "s|{{AUTHENTIK_BOOTSTRAP_TOKEN}}|${AUTHENTIK_BOOTSTRAP_TOKEN}|g" \
      -e "s|{{HOMARR_OIDC_CLIENT_ID}}|${HOMARR_OIDC_CLIENT_ID}|g" \
      -e "s|{{HOMARR_OIDC_CLIENT_SECRET}}|${HOMARR_OIDC_CLIENT_SECRET}|g" \
      -e "s|{{AUDIOBOOKSHELF_OIDC_CLIENT_ID}}|${AUDIOBOOKSHELF_OIDC_CLIENT_ID}|g" \
      -e "s|{{AUDIOBOOKSHELF_OIDC_CLIENT_SECRET}}|${AUDIOBOOKSHELF_OIDC_CLIENT_SECRET}|g" \
      -e "s|{{NEXTCLOUD_OIDC_CLIENT_ID}}|${NEXTCLOUD_OIDC_CLIENT_ID}|g" \
      -e "s|{{NEXTCLOUD_OIDC_CLIENT_SECRET}}|${NEXTCLOUD_OIDC_CLIENT_SECRET}|g" \
      -e "s|{{GRAFANA_OIDC_CLIENT_ID}}|${GRAFANA_OIDC_CLIENT_ID}|g" \
      -e "s|{{GRAFANA_OIDC_CLIENT_SECRET}}|${GRAFANA_OIDC_CLIENT_SECRET}|g" \
      -e "s|{{VAULTWARDEN_OIDC_CLIENT_ID}}|${VAULTWARDEN_OIDC_CLIENT_ID}|g" \
      -e "s|{{VAULTWARDEN_OIDC_CLIENT_SECRET}}|${VAULTWARDEN_OIDC_CLIENT_SECRET}|g" \
      -e "s|{{TZ}}|${TZ}|g" \
      "$tmpl" > "$output"
    info "  $(basename "$tmpl") → $(basename "$output")"
  done

  success "Templates processed"
}

start_stack() {
  step "Starting the stack"

  info "[1/5] Pulling images (this may take several minutes)..."
  docker compose pull --quiet

  info "[2/5] Starting databases..."
  docker compose up -d postgres redis

  info "      Waiting for PostgreSQL..."
  local retries=0
  until docker compose exec -T postgres pg_isready -U postgres &>/dev/null; do
    retries=$((retries+1))
    [[ $retries -gt 30 ]] && die "PostgreSQL did not become healthy in time"
    sleep 2
  done
  success "      PostgreSQL ready"

  info "      Waiting for Redis..."
  retries=0
  until docker compose exec -T redis redis-cli -a "${REDIS_PASSWORD}" ping &>/dev/null; do
    retries=$((retries+1))
    [[ $retries -gt 20 ]] && die "Redis did not become healthy in time"
    sleep 2
  done
  success "      Redis ready"

  info "[3/5] Starting Authentik (first run takes ~90s)..."
  docker compose up -d authentik-server authentik-worker

  info "      Waiting for Authentik API..."
  retries=0
  until curl -sf --max-time 5 "http://localhost:9000/api/v3/root/config/" &>/dev/null 2>&1; do
    retries=$((retries+1))
    [[ $retries -gt 60 ]] && {
      warn "Authentik is slow to start. Check: docker compose logs authentik-server"
      warn "You can run 'docker compose up -d' manually once Authentik is ready."
      return 0
    }
    sleep 3
  done
  success "      Authentik ready"

  info "[4/5] Starting remaining services..."
  docker compose up -d

  info "[5/5] Waiting for all services to stabilize..."
  sleep 10

  success "Stack started"
}

