GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

print_banner() {
  echo -e "${BLUE}${BOLD}"
  cat << 'BANNER'
╔══════════════════════════════════════════════════════════════╗
║           Ultimate Self-Hosted Stack Installer               ║
║                                                              ║
║  Traefik · Authentik · Homarr · Jellyfin · Nextcloud        ║
║  Navidrome · Sonarr/Radarr/Lidarr · Headscale · VPN        ║
║  Vaultwarden · Grafana · Uptime Kuma · and more...          ║
╚══════════════════════════════════════════════════════════════╝
