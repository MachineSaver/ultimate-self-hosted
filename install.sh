create_directories() {
  step "Creating directory structure"

  local dirs=(
    data/traefik
    data/authentik/media
    data/authentik/custom-templates
    data/homarr/appdata
    data/jellyfin/config
    data/jellyfin/cache
    data/jellyseerr
