    data/booklore-bookdrop
    data/navidrome
    data/sonarr
    data/radarr
    data/lidarr
    data/prowlarr
    data/qbittorrent
    data/nextcloud
    data/headscale
    data/wireguard
    data/uptime-kuma
    data/prometheus
    data/grafana
    data/vaultwarden
    data/media/movies
    data/media/tv
    data/media/music
    data/media/books
    data/media/audiobooks
    data/media/podcasts
    data/downloads/complete
    data/downloads/incomplete
  )

  for d in "${dirs[@]}"; do
    mkdir -p "$d"
  done

  # acme.json must be 600 or Traefik refuses to use it
  touch data/traefik/acme.json
  chmod 600 data/traefik/acme.json

  # Grafana runs as UID/GID 472 — fix ownership so it can write its data dir
  chown -R 472:472 data/grafana 2>/dev/null || true

  # Pre-seed ARR service configs so they start with External auth.
  # Without this they default to Forms auth and show a second login screen
  # even though Authentik forward-auth is already protecting the route.
  # Only written on fresh install — never overwrites an existing config.
  local arr_xml
  arr_xml='<?xml version="1.0" encoding="utf-8"?>
<Config>
  <AuthenticationMethod>External</AuthenticationMethod>
  <AuthenticationRequired>Enabled</AuthenticationRequired>
  <LogLevel>info</LogLevel>
  <AnalyticsEnabled>False</AnalyticsEnabled>
</Config>'

  for svc in sonarr radarr lidarr prowlarr; do
    local cfg="data/${svc}/config.xml"
    if [[ ! -f "$cfg" ]]; then
      echo "$arr_xml" > "$cfg"
      chown "${PUID}:${PGID}" "$cfg" 2>/dev/null || true
      info "  Pre-configured ${svc}: auth=External"
    fi
  done

  success "Directories created"
}

