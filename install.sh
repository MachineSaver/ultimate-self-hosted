}

info()    { echo -e "${CYAN}[INFO]${NC}  $*"; }
success() { echo -e "${GREEN}[OK]${NC}    $*"; }
warn()    { echo -e "${YELLOW}[WARN]${NC}  $*"; }
error()   { echo -e "${RED}[ERROR]${NC} $*" >&2; }
step()    { echo -e "\n${BOLD}${BLUE}▶ $*${NC}"; }

die() { error "$*"; exit 1; }

check_requirements() {
  step "Checking requirements"
  local missing=()

  command -v docker   &>/dev/null || missing+=("docker")
  docker compose version &>/dev/null 2>&1 || missing+=("docker-compose-plugin (v2)")
  command -v curl     &>/dev/null || missing+=("curl")
  command -v openssl  &>/dev/null || missing+=("openssl")
  command -v python3  &>/dev/null || warn "python3 not found — WireGuard password hash will be skipped"

  if [[ ${#missing[@]} -gt 0 ]]; then
    die "Missing required tools: ${missing[*]}"
  fi

  # Verify Docker daemon is running
  docker info &>/dev/null || die "Docker daemon is not running"

  success "All requirements met"
}

gen_secret()   { openssl rand -hex 32; }
gen_short_id() { openssl rand -hex 8; }

prompt_config() {
  step "Configuration"
  echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

  while [[ -z "${ADMIN_USER:-}" ]]; do
    read -rp "$(echo -e "${YELLOW}Admin username${NC}: ")" ADMIN_USER
  done

  while true; do
    read -rsp "$(echo -e "${YELLOW}Admin password${NC}: ")" ADMIN_PASSWORD; echo
    read -rsp "$(echo -e "${YELLOW}Confirm password${NC}: ")" _confirm; echo
    [[ "$ADMIN_PASSWORD" == "$_confirm" ]] && [[ -n "$ADMIN_PASSWORD" ]] && break
    warn "Passwords do not match or are empty — try again"
  done

  while [[ -z "${DOMAIN:-}" ]]; do
    read -rp "$(echo -e "${YELLOW}Fully qualified domain (e.g. example.com)${NC}: ")" DOMAIN
    # Strip protocol/trailing slash
    DOMAIN="${DOMAIN#https://}"; DOMAIN="${DOMAIN#http://}"; DOMAIN="${DOMAIN%/}"
  done

  read -rp "$(echo -e "${YELLOW}Admin email${NC} [admin@${DOMAIN}]: ")" ADMIN_EMAIL
  ADMIN_EMAIL="${ADMIN_EMAIL:-admin@${DOMAIN}}"

  read -rp "$(echo -e "${YELLOW}Timezone${NC} [America/New_York]: ")" TZ
  TZ="${TZ:-America/New_York}"

  read -rp "$(echo -e "${YELLOW}Media directory${NC} [./data/media]: ")" MEDIA_DIR
  MEDIA_DIR="${MEDIA_DIR:-./data/media}"

  read -rp "$(echo -e "${YELLOW}PUID${NC} [1000]: ")" PUID
  PUID="${PUID:-1000}"

  read -rp "$(echo -e "${YELLOW}PGID${NC} [1000]: ")" PGID
  PGID="${PGID:-1000}"

  echo ""
  success "Configuration captured"
}

generate_secrets() {
  step "Generating secrets"

  POSTGRES_PASSWORD=$(gen_secret)
  REDIS_PASSWORD=$(gen_secret)
  AUTHENTIK_SECRET_KEY=$(gen_secret)
  AUTHENTIK_BOOTSTRAP_TOKEN=$(gen_secret)
  NEXTCLOUD_DB_PASSWORD=$(gen_secret)

  HOMARR_OIDC_CLIENT_ID="homarr-$(gen_short_id)"
  HOMARR_OIDC_CLIENT_SECRET=$(gen_secret)

  AUDIOBOOKSHELF_OIDC_CLIENT_ID="abs-$(gen_short_id)"
  AUDIOBOOKSHELF_OIDC_CLIENT_SECRET=$(gen_secret)

  NEXTCLOUD_OIDC_CLIENT_ID="nextcloud-$(gen_short_id)"
  NEXTCLOUD_OIDC_CLIENT_SECRET=$(gen_secret)

  GRAFANA_OIDC_CLIENT_ID="grafana-$(gen_short_id)"
  GRAFANA_OIDC_CLIENT_SECRET=$(gen_secret)

  VAULTWARDEN_OIDC_CLIENT_ID="vaultwarden-$(gen_short_id)"
  VAULTWARDEN_OIDC_CLIENT_SECRET=$(gen_secret)

  # WireGuard bcrypt password hash
  if command -v python3 &>/dev/null && python3 -c "import bcrypt" &>/dev/null 2>&1; then
    WG_PASSWORD_HASH=$(python3 -c "import bcrypt; print(bcrypt.hashpw('${ADMIN_PASSWORD}'.encode(), bcrypt.gensalt(12)).decode())")
  elif command -v python3 &>/dev/null; then
    python3 -m pip install bcrypt -q 2>/dev/null || true
    WG_PASSWORD_HASH=$(python3 -c "import bcrypt; print(bcrypt.hashpw('${ADMIN_PASSWORD}'.encode(), bcrypt.gensalt(12)).decode())" 2>/dev/null || echo "")
  fi
  WG_PASSWORD_HASH="${WG_PASSWORD_HASH:-}"

  success "Secrets generated"
}

