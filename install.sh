start_stack() {
  step "Starting the stack"

  info "[1/5] Pulling images (this may take several minutes)..."
  docker compose pull --quiet

  info "[2/5] Starting databases..."
  docker compose up -d postgres redis

  info "      Waiting for PostgreSQL..."
  local retries=0
  until docker compose exec -T postgres pg_isready -U postgres &>/dev/null; do
    retries=$((retries+1))
    [[ $retries -gt 30 ]] && die "PostgreSQL did not become healthy in time"
    sleep 2
  done
  success "      PostgreSQL ready"

  info "      Waiting for Redis..."
  retries=0
  until docker compose exec -T redis redis-cli -a "${REDIS_PASSWORD}" ping &>/dev/null; do
    retries=$((retries+1))
    [[ $retries -gt 20 ]] && die "Redis did not become healthy in time"
    sleep 2
  done
  success "      Redis ready"

  info "[3/5] Starting Authentik (first run takes ~90s)..."
  docker compose up -d authentik-server authentik-worker

  info "      Waiting for Authentik API (first run runs DB migrations — allow ~90s)..."
  retries=0
  until docker compose exec -T authentik-server /lifecycle/ak healthcheck &>/dev/null 2>&1; do
    retries=$((retries+1))
    [[ $retries -gt 60 ]] && die "Authentik did not become healthy after ~3 minutes. Check: docker compose logs authentik-server"
    sleep 3
  done
  success "      Authentik ready"

  if [[ "${ADMIN_USER}" != "akadmin" ]]; then
    info "      Renaming Authentik default user 'akadmin' → '${ADMIN_USER}'..."
    # Bootstrap writes akadmin AFTER the healthcheck passes — retry until the token is valid
    local user_pk="" rename_retries=0
    until [[ -n "$user_pk" ]] || [[ $rename_retries -gt 15 ]]; do
      user_pk=$(docker compose exec -T authentik-server \
        curl -sf \
          -H "Authorization: Bearer ${AUTHENTIK_BOOTSTRAP_TOKEN}" \
          "http://localhost:9000/api/v3/core/users/?username=akadmin" \
        | python3 -c "import sys,json; d=json.load(sys.stdin); print(d['results'][0]['pk'])" 2>/dev/null || true)
      [[ -z "$user_pk" ]] && { rename_retries=$((rename_retries+1)); sleep 3; }
    done

    if [[ -n "$user_pk" ]]; then
      docker compose exec -T authentik-server \
        curl -sf -X PATCH \
          -H "Authorization: Bearer ${AUTHENTIK_BOOTSTRAP_TOKEN}" \
          -H "Content-Type: application/json" \
          -d "{\"username\": \"${ADMIN_USER}\", \"name\": \"${ADMIN_USER}\"}" \
          "http://localhost:9000/api/v3/core/users/${user_pk}/" > /dev/null \
        && success "      Authentik admin username set to '${ADMIN_USER}'" \
        || warn "Could not rename akadmin — log in to Authentik as 'akadmin' and rename manually"
    else
      warn "Could not find akadmin user after 45s — log in to Authentik as 'akadmin' and rename manually"
    fi
  fi

  info "[4/5] Starting remaining services..."
  docker compose up -d

  info "[5/5] Waiting for all services to stabilize..."
  sleep 10

  success "Stack started"
}

configure_services() {
  step "Configuring services"

  local all_ok=true

  run_config_script() {
    local label="$1" script="$2"
    info "${label}..."
    if bash "${script}"; then
      success "      ${label} — done"
    else
      warn "      ${label} failed — re-run: ${script}"
      all_ok=false
    fi
  }

  run_config_script "Nextcloud OIDC"          scripts/configure-nextcloud-oidc.sh
  run_config_script "Audiobookshelf OIDC"     scripts/configure-audiobookshelf-oidc.sh
  run_config_script "Uptime Kuma admin"       scripts/configure-uptime-kuma.sh
  run_config_script "qBittorrent credentials" scripts/configure-qbittorrent.sh

  info "Headscale user + pre-auth key..."
  bash scripts/configure-headscale.sh || {
    warn "      Headscale setup failed — re-run: scripts/configure-headscale.sh"
    all_ok=false
  }

  [[ "$all_ok" == "true" ]] \
    && success "All services configured" \
    || warn "Some configuration steps failed — see warnings above"
}

